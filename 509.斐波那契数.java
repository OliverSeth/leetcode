// @lc code=start
class Solution {
    public int fib(int n) {
        int[] arr = new int[Math.max(2, n + 1)];
        arr[0] = 0;
        arr[1] = 1;
        for (int i = 2; i < arr.length; i++) {
            arr[i] = arr[i - 2] + arr[i - 1];
        }
        return arr[n];
    }
}
